-- Cập nhật tên cho nhân viên
-- Cú pháp: UPDATE employees SET name = 'Tên mới' WHERE id = [ID nhân viên];

-- Ví dụ cập nhật tên cho nhân viên có ID = 1
UPDATE employees SET name = 'Nguyễn Văn A' WHERE id = 1;

-- Ví dụ cập nhật tên cho nhân viên có ID = 2
UPDATE employees SET name = 'Trần Thị B' WHERE id = 2;

